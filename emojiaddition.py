# Added the logic for the mode of transportation emoji to show up when seeing the results of the carbon emissions for each respective mode of transportation.
def rainemoji(flight, train, vehicle):
    flightemoji(
        emoji="✈️",
        font_size=54,
        falling_speed=3,
        animation_length="5s",
    )
    train(
        emoji="✈️",
        font_size=54,
        falling_speed=3,
        animation_length="5s",
    )
    vehicle(
        emoji="✈️",
        font_size=54,
        falling_speed=3,
        animation_length="5s",
    )
